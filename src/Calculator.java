class Calculator {
        boolean isEven(int x) {
            return x % 2 == 0;
        }
        boolean isOdd(int y) {
            return y % 2 != 0;
        }
        double circleField(double r) {
            return 3.14 * (r * r);
        }
        int power(Integer z) {
            return z * z;
        }
}
