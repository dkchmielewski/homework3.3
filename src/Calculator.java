class Calculator {
        boolean isEven(int x) {
            return x % 2 == 0;
        }
        boolean isOdd(int y) {
            return y % 2 != 0;
        }
        double circleField(double r) {
            Double result = 0.0;
            result = 3.14 * (r * r);
            return result;
        }
        int power(Integer z) {
            Integer result = z * z;
            return result;
        }
}
